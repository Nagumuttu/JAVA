package com.typecasting;

public class ImplicitTypeCasting {
	public static void main(String[] args) {
		byte var1 = 10;
		System.out.println("Byte type:"+var1);
		
		int var2 = var1;
		System.out.println("int type:"+var2);
		
		double var3=30;
		System.out.println("double type:"+var3);
		
		short var4=(short)var3;
		System.out.println("short type:"+var4);
		
	}

}
