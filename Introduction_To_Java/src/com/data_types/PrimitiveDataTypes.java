package com.data_types;

public class PrimitiveDataTypes {
	public static void main(String[] args) {
		
	
	byte age = 120;
	System.out.println("Your age is: "+age);
	
	short i = 12;
	System.out.println("Your i is: "+i);
	
	int salary = 12000;
	System.out.println("Your salary is: "+salary);
	
	long years = 120;
	System.out.println("Your years is: "+years);
	
	float num = 5.2f;
	System.out.println("Your hieght is: "+num);
	
	double  d= 17000;
	System.out.println("Your income is: "+d);
	
	char letter = 'a';
	System.out.println("written letter is: "+letter);
	
	boolean java = true;
	System.out.println("true/false: "+java);
	
}
}